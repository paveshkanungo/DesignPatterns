package org.example.Behavioral.Observer;

public class Definition {
    void definitionObserverPattern(){
        // The Observer Pattern is a behavioral design pattern where an object (aka the "subject" or
        // "observable" or “publisher”) maintains a list of dependents (called "observers") and
        // automatically notifies them whenever there is a change in its state. The pattern also allows
        // addition and removal of observers at runtime.

        // Real-life Examples:
        // Real-life examples of the Observer pattern include:
        // 1. Weather Applications: Where multiple devices receive updates from a weather station.
        // 2. Social Media Feeds: When we follow someone on Instagram, Facebook, or Twitter, we become
        //      observer of their profile. When they post new content, we are automatically notified.
        // 3. Subscription Services: YouTube subscriptions, where viewers are notified of new videos, or Content
        //      magazine/newspaper/newsletter subscriptions, where publishers send new issues to subscribers.
        // 4. Stock Market Trackers: When the price of a stock (or state) changes, the stock's market system
        //      (the observable) sends out notifications to all interested investors (the observers).
    }
}
